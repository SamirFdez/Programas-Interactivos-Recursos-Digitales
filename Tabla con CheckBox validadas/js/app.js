function validarRespuestas(){

    i1 = document.getElementById("i1").value;
    if(i1.checked){
        $("#text1").removeClass("normal");
        $("#text1").removeClass("mala");
        $("#text1").addClass("buena");
    } else {
        $("#text1").removeClass("normal");
        $("#text1").addClass("mala");
    }
}

